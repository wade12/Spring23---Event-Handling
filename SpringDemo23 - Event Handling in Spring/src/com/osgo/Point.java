package com.osgo;

public class Point
{
	private int x;
	private int y;
	
	public int getX()
	{
		return x;
	}// end method getX
	
	public void setX(int x)
	{
		this.x = x;
	}// end method setX
	
	public int getY()
	{
		return y;
	}// end method getY
	
	public void setY(int y)
	{
		this.y = y;
	}// end method setY
	
} // end Class Point
